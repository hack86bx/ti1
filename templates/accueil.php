    <p>Budapest (prononcé [by.da.ˈpɛst], hongrois : Budapest [ˈbu.dɒ.pɛʃt] Écouter ; allemand : Budapest ou anciennement Ofen-Pesth) est la plus grande ville et la capitale de la Hongrie. Elle se situe en aval du coude du Danube entre le massif de Transdanubie et l'Alföld. Ses habitants sont les Budapestois (en hongrois, budapesti, -ek).La ville actuelle est créée le 17 novembre 1873 par la fusion de Buda — alors capitale de la Hongrie —, de Pest et d'Óbuda1. Elle a pour origine le site d'Aquincum2, un point de peuplement celte3 devenu capitale de la Pannonie inférieure pendant l'époque romaine3. Les Magyars arrivent dans la région au ixe siècle. Leur premier point d'implantation est pillé par les Mongols en 1241-12424. La ville est reconstruite et devient l'un des centres de la culture humaniste de la Renaissance5 au xve siècle6. Après près de 150 ans de domination ottomane, elle poursuit son développement et connaît son apogée avec l'épanouissement de l'ère industrielle aux xviiie et xixe siècles. Après la fusion de 1873 et l'accession de la ville au rang de seconde capitale de l'Autriche-Hongrie, Budapest atteint les proportions et les caractéristiques d'une ville mondiale7. Marquée par les différentes traces léguées par l'histoire, Budapest a notamment été l'épicentre de la révolution hongroise de 1848, de la république des conseils de Hongrie de 1919, de l'opération Panzerfaust en 1944, de la bataille de Budapest de 1945 et de l'insurrection de 1956.

Considérée comme l'une des plus belles villes d'Europe et comme la « perle » du Danube8,1,9, son panorama, le quartier du château de Buda, l'avenue Andrássy et le métropolitain du Millénaire figurent au patrimoine mondial de l'UNESCO8,10. Destination touristique importante, la ville attire plus de 4,3 millions de visiteurs par an11.</p>
    
